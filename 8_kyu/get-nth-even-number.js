const nthEven = n => (n - 1) * 2

// Output:
console.log(nthEven(1))  // expected output: 0
console.log(nthEven(2))  // expected output: 2
console.log(nthEven(3))  // expected output: 4
console.log(nthEven(100))  // expected output: 198
console.log(nthEven(1298734))  // expected output: 2597466